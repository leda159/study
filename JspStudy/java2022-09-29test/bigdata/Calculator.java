package bigdata;

public class Calculator {

	public int add(int num1, int num2) {
		return num1+num2;
	}
	public int minus(int num1, int num2) {
		return num1-num2;
	}
	public int mul(int num1, int num2) {
		return num1*num2;
	}
	public double div(int num1, int num2) {
		return (double)num1/num2;
	}

}